    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        clearAll();
        try {
            userId = txtUserId.getText();
            ArrayList <UserProfile> userList = UserDao.getSingleUserData(userId);
            
            if(userList.isEmpty() == true){
                JOptionPane.showMessageDialog(null, "Sorry! User not found","Error!",JOptionPane.INFORMATION_MESSAGE);
                txtUserId.setText("");
            }
            else {
                Object[] rows = new Object[3];
                DefaultTableModel model = (DefaultTableModel)jtUserDetail.getModel();
                for(UserProfile up: userList) {
                    rows[0] = up.getUserid();
                    rows[1] = up.getUsername();
                    rows[2] = up.getEmpid();                                        
                    model.addRow(rows);                 
                }   
            }    
        }
    
        catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error while connecting to DB","Exception!",JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

    public void clearAll() {        
        DefaultTableModel model = (DefaultTableModel)jtUserDetail.getModel();                            
        model.setRowCount(0);
    }
    
    
