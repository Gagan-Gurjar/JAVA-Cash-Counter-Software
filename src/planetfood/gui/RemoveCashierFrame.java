    private void btnDeleteCashierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCashierActionPerformed
        try { 
            String userId = txtUserId.getText();
            if(userId.isEmpty()) {                
                JOptionPane.showMessageDialog(null, "Please Enter UserId !","Error!",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            
            boolean status = UserDao.removeCashier(userId);
            if(status) {
                JOptionPane.showMessageDialog(null, "Cashier Successfully Removed!","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                txtUserId.setText("");
            }
            else
                JOptionPane.showMessageDialog(null, "Cashier Not Removed!","Error!",JOptionPane.ERROR_MESSAGE);
            
        }
        catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error while deleting product!","Exception!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteCashierActionPerformed

